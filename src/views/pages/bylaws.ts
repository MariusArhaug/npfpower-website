export function bylawsPage(): string {
  return `
    <section class="page-section">
      <h1>NPF Bylaws</h1>
      <div class="content-block bylaws">

        <h2>§1 – Definition and Name</h2>
        <p>The Nordic Powerlifting Federation (NPF) is an organization founded on February 23, 1975. The Federation is composed of the affiliated Nordic National Federations governing the sport of powerlifting based on one national federation per country. The NPF will work for increased interest of powerlifting in the Nordic countries.</p>

        <h2>§2 – Objectives</h2>
        <p>NPF shall follow the rules defined by the International Powerlifting Federation (IPF), but the NPF can make adjusted rules for the Nordic Championship.</p>

        <h2>§3 – NPF Congress</h2>
        <p>The NPF congress will be held one day before the Nordic Championship every second year.</p>
        <p>The President, General Secretary (GS) and the other nations' members of the NPF board shall be present at the congress.</p>
        <p>Proposals to the congress must be sent to the President and the GS not later than 4 weeks prior to the congress.</p>
        <p>An official agenda for the NPF congress must be sent at least 2 weeks before the congress takes place.</p>
        <p>Every nation has one vote for the congress as long as payments according to §7 are fulfilled. Each nation can nominate up to two delegates for the NPF congress.</p>

        <h2>§4 – Order of Business</h2>
        <ol>
          <li>The President opens the congress</li>
          <li>Approve the order of business</li>
          <li>Elect speaker and secretary for the congress</li>
          <li>Approve the minutes of the last congress</li>
          <li>Previous years report</li>
          <li>Previous years financial report</li>
          <li>Set fees (annual nations member fee, nomination fees and doping fee)</li>
          <li>Set travel rates</li>
          <li>Proposals</li>
          <li>Budget</li>
          <li>Time and place for future championships and congress</li>
          <li>Election (see §5)</li>
          <li>Any other business</li>
        </ol>

        <h2>§5 – Election</h2>
        <p>The NPF board is elected for two (2) years. The NPF board includes one President and one General Secretary from the same country, in addition to national representatives from the other member nations.</p>

        <h2>§6 – Duties</h2>
        <h3>President</h3>
        <ul>
          <li>Representing the NPF between the congresses</li>
          <li>Lead the NPF board</li>
          <li>Organizing the congress</li>
          <li>Approve all financial transactions together with the GS</li>
        </ul>
        <h3>General Secretary</h3>
        <ul>
          <li>Prepare the yearly activity report and financial report</li>
          <li>Act as treasurer</li>
          <li>Approve all financial transactions together with the President</li>
        </ul>

        <h2>§7 – Fees</h2>
        <p>Every member nation must pay the annual fee to the NPF at latest end of January. The annual fee is 550€ (as of 2025 congress). Nomination fee: 45€ per lifter (powerlifting and bench press).</p>

        <h2>§9 – Championships</h2>
        <p>Nordic Championships for Sub-juniors and Juniors are sanctioned by the EPF and doping tests are executed in accordance with the rules of IPF and WADA.</p>
        <p>Championships shall be organized every year in preferably mid-September.</p>

        <h2>§10 – Nordic Records</h2>
        <p>Nordic records are listed in squat, bench press, single bench press, deadlift and total. Nordic records demand ruling of international category II referees.</p>
        <p>Records must be sent to the NPF's record registrar no later than one (1) week after the record has been broken.</p>

        <h2>§12 – Changes in the Bylaws</h2>
        <p>In order to change the bylaws, any proposal needs at least sixty (60)% of the total number of votes in favor. The bylaws can only be modified at the congress (odd years).</p>

        <h2>§13 – Dissolution</h2>
        <p>NPF can only be dissolved when all member nations support the decision of dissolution.</p>
      </div>
    </section>
  `;
}
